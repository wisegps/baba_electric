package com.wise.baba.entity;

public class ElectricCarDetail {
	
	private String xhlc;//续航里程
	private String sydl;//剩余电池量%
	private String dpdy;//工作电压
	
	
	
	
	public String getXhlc() {
		return xhlc;
	}
	public void setXhlc(String xhlc) {
		this.xhlc = xhlc;
	}
	public String getSydl() {
		return sydl;
	}
	public void setSydl(String sydl) {
		this.sydl = sydl;
	}
	public String getDpdy() {
		return dpdy;
	}
	public void setDpdy(String dpdy) {
		this.dpdy = dpdy;
	}
	
}
